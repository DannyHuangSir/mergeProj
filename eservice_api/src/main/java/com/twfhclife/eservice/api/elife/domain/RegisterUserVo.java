public class RegisterUserVo {
	
	private String rocId;
	
	private String email;
	
	private String mobile;
	
	private String lomsInsuNo;
	
	private String lomsAmt;
	
	private String lipmInsuNo;

	public String getRocId() {
		return rocId;
	}

	public void setRocId(String rocId) {
		this.rocId = rocId;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getLomsInsuNo() {
		return lomsInsuNo;
	}

	public void setLomsInsuNo(String lomsInsuNo) {
		this.lomsInsuNo = lomsInsuNo;
	}

	public String getLomsAmt() {
		return lomsAmt;
	}

	public void setLomsAmt(String lomsAmt) {
		this.lomsAmt = lomsAmt;
	}

	public String getLipmInsuNo() {
		return lipmInsuNo;
	}

	public void setLipmInsuNo(String lipmInsuNo) {
		this.lipmInsuNo = lipmInsuNo;
	}
	
}
