$(document).ready(function(){

  // Tab
  $("#tab li:first").addClass("active");
  $(".tab_content").hide();
  $(".tab_content:first").show();

  $("#tab li").click(function(e){
   var index = $(this).index();
   $("#tab li").removeClass("active");
   $(this).addClass("active");
   $(".tab_content").hide();
   $(".tab_content:eq(" + index + ")").show();
   return false
  });

  // Animation
  $.fn.extend({
    animateCss: function (animationName) {
      var animationEnd = 'webkitAnimationEnd mozAnimationEnd MSAnimationEnd oanimationend animationend';
      $(this).addClass('animated ' + animationName).one(animationEnd, function() {
        $(this).removeClass('animated ' + animationName);
      });
    }
  });

  var canvas = $('.canvasWrap'),
      inner = $('.innerCanvas'), // get page wrapper
      body = $('body'),
      toggle = $('.toggle, .overlay');

  toggle.on('click', function(e){
    var innerWidth = inner.width();
    e.preventDefault();
    body.toggleClass('offcanvas-active');

    // toggle.toggleClass('x');

    if (body.hasClass('offcanvas-active')) {
        inner.css('width',innerWidth );
    } else {
        inner.css('width', 'auto');
    }

  }); // toggle


  var docHeight = $(window).height();
  var footerHeight = $('footer').height();
  var footerTop = $('footer').position().top + footerHeight;

  if (footerTop < docHeight) {
   $('footer').css('margin-top', 40 + (docHeight - footerTop) + 'px');
  }

  // input focus
  $('.fieldContent input, .fieldContent select').focus(function () {
  		$(this).parents('.fieldContent').addClass('inputFocus');
  	}).blur(function () {
  		$(this).parents('.fieldContent').removeClass('inputFocus');
  });
  $('.indexForm input, .indexForm select').focus(function () {
  		$(this).parents('.indexForm').addClass('inputFocus');
  	}).blur(function () {
  		$(this).parents('.indexForm').removeClass('inputFocus');
  });

  var windowWidth = $(window).width();
  if ( windowWidth <= 991 ) {
    $('body').addClass('mobileDevice');
    $('.portalType').click(function(){
      $(this).toggleClass('active');
      $(this).find('.portalTypeContent').toggle().animateCss('fadeIn');
    });
  }; // <= 991



  // Resize Function
  $(window).on('resize', function(){
    inner.css('width', 'auto');
    body.removeClass('offcanvas-active');
    // toggle.removeClass('x');
  }) // resize

});

function logoSwitch() {
  if ( $(window).width() <= 768 ) {
    $('.headLogo img').attr('src', 'images/logo_w.png');
    $('.progress > div').removeClass('container');
  } else {
    $('.headLogo img').attr('src', 'images/logo.png');
  }
}

$(window).resize(logoSwitch);
$(document).ready(logoSwitch);

// Go to Top
$(window).scroll(function() {
  if ($(this).scrollTop() > 200) {
    $('.goTop').fadeIn(200);
  } else {
    $('.goTop').fadeOut(200);
  }
});
$('.goTop').click(function(e) {
  e.preventDefault();
  $('html, body').animate({scrollTop: 0}, 300);
});

// Form Finished hide
$('.hasFold h3.formTitle').click(function(){
    $(this).parent('.formSet').toggleClass('finished');
});

